def tr(text, language="en", *args, **kwargs):
    try:
        lang_norm = str(language) if language is not None else "en"
    except Exception:
        lang_norm = "en"
    lang_norm = lang_norm.strip()

    if "_" in lang_norm:
        base = lang_norm.split("_")[0]
    else:
        base = lang_norm
    base = base.lower()

    lang_key = ""
    if base == "pt":
        lang_key = "pt_BR"
    elif base in ("zh", "zh-cn", "zh_cn", "zh-hans", "zh-hant"):
        lang_key = "zh"
    elif base == "ru":
        lang_key = "ru"
    elif base == "en":
        lang_key = "en"
    else:

        if lang_norm in translations:
            lang_key = lang_norm
        else:
            lang_key = "en"

    lang_dict = translations.get(lang_key, {})
    translated_text = lang_dict.get(text)

    if translated_text is None:
        translated_text = text

    if args or kwargs:
        try:
            return translated_text.format(*args, **kwargs)
        except (KeyError, IndexError, ValueError, TypeError):

            return translated_text
    else:
        return translated_text
