def tr(text, language='en', *args, **kwargs):
    lang_dict = translations.get(language, translations.get('en', {}))
    translated_text = lang_dict.get(text)
    if translated_text is None and language != 'en':
        translated_text = translations.get('en', {}).get(text)
    if translated_text is None:
        translated_text = text
    if args or kwargs:
        try:
            return translated_text.format(*args, **kwargs)
        except (KeyError, IndexError, ValueError) as e:
            print(f"Translation formatting error for text '{text}' in lang '{language}': {e}")
            return translated_text
    else:
        return translated_text
